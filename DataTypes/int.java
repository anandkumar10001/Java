//Byte
public class ByteExample {
    public static void main(String[] args) {
        byte a = -128; //min
        byte b = 20;
        byte c = 127; //max
        // byte d = 128;  // Error: out of range

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
    }
}